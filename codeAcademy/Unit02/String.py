# Strings and concatenation
word = "First word"
word2 = "Second word"
print(word + " " + word2)

# Escaping characters
name = "Renata"
print('I\'m ' + name)

# String methods
name = "Renata"
print(name.upper())
print(name.lower())
print(name.title())

# String formatting
name = "Renata"
print(f"I'm {name}")

# Accessing characters
name = "Renata"
print(name[0])
print(name[1])
print(name[2])

# String slicing
name = "Renata"
print(name[0:3])
print(name[0:4])
print(name[0:5])